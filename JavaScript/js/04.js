resultat = nb1 + nb2;
console.log( "L'addition de nb1 et nb2 = " + resultat);

//## ~ La Soustraction ~ ## //

resultat = nb1 - nb2;
console.log( "La soustraction de nb1 et nb2 = " + resultat);

//## ~ La Multiplication ~ ## //

resultat = nb1 * nb2;
console.log( "La multiplaction de nb1 et nb2 = " + resultat);

//## ~ La Division ~ ## //

resultat = nb1 / nb2;
console.log( "La division de nb1 et nb2 = " + resultat);

//## ~ Le modulo (c'est le reste de la division) ~ ## //

nb1 = 11;
resultat = nb1 % nb2;
console.log( "La modulo de nb1 et nb2 = " + resultat);